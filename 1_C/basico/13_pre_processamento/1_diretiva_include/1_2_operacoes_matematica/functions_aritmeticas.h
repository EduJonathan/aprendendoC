#ifndef FUNCTIONS_ARITMETICAS_H
#define FUNCTIONS_ARITMETICAS_H

/**
 * @brief Protótipo de função para adição de dois números
 *
 * @param a Primeiro valor a ser inserido
 * @param b Segundo valor a ser inserido
 * @return float Retorna a soma dos dois parâmetros
 */
float add(float a, float b);

/**
 * @brief Protótipo de função para subtração de dois números
 *
 * @param a Primeiro valor a ser inserido
 * @param b Segundo valor a ser inserido
 * @return float Retorna a subtração dos dois parâmetros
 */
float subtract(float a, float b);

/**
 * @brief Protótipo de função para multiplicação de dois números
 *
 * @param a Primeiro valor a ser inserido
 * @param b Segundo valor a ser inserido
 * @return float Retorna a multiplicação dos dois parâmetros
 */
float multiply(float a, float b);

/**
 * @brief Protótipo de função para divisão de dois números
 *
 * @param a Primeiro valor a ser inserido
 * @param b Segundo valor a ser inserido
 * @return float Retorna a divisão de a por b
 */
float divide(float a, float b);

#endif
